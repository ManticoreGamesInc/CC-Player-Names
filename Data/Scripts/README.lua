The Player Names component is a lightweight component for showing the names above the player's heads. This is 
useful for players to be able to see who is in the game and where they are.
-----------------------------------------------------------------------------------------------------------------

==========
How to use
==========

Drop the Player Names template into the hierarchy.

To modify the look of the name, there is a template that can be either modified or switched out by changing
the custom property on the root of the template.

- Template

The template to use when creating a player's name.

